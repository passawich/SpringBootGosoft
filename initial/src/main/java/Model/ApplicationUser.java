package Model;

public class ApplicationUser {
		private String user;
		private String pass;
		
		public String getUser() {
			return this.user;
		}
		public void setUser(String user) {
			this.user = user;
		}
		public String getPass() {
			return this.pass;
		}
		public void setPass(String pass) {
			this.pass = pass;
		}
}
